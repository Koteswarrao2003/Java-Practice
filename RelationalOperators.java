/*
RELATIONAL OPERATORS IN JAVA

Relational operators are used to compare
two values and return true or false.

Operators used in this program:

1) ==  → Equal to
2) !=  → Not equal to
3) >   → Greater than
4) <   → Less than
5) >=  → Greater than or equal to
6) <=  → Less than or equal to

The program compares two numbers (a and b)
and prints the result of each comparison.
*/

public class RelationalOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
